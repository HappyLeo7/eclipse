package exx05_O;

public class FirstString {

	String startsWith(String s){
		return Character.toString(s.charAt(0));
	}
	String startsWith1(String s){
		return Character.toString(s.charAt(1));
	}
	String startsWith2(String s){
		return Character.toString(s.charAt(2));
	}
	String startsWith3(String s){
		return Character.toString(s.charAt(3));
	}
}
