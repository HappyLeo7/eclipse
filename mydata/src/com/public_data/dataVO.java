package com.public_data;

public class dataVO {

	//테이블 데이터
	private int no; //넘버
	private String title; // 제목
	private String registration_date; // 등록날짜
	private String views ;// 조회수
	private String institution_name; // 기관명
	private String department; // 부서
	private String manager; // 담당자
	private String management_number; // 관리번호
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getRegistration_date() {
		return registration_date;
	}
	public void setRegistration_date(String registration_date) {
		this.registration_date = registration_date;
	}
	public String getViews() {
		return views;
	}
	public void setViews(String views) {
		this.views = views;
	}
	public String getInstitution_name() {
		return institution_name;
	}
	public void setInstitution_name(String institution_name) {
		this.institution_name = institution_name;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getManagement_number() {
		return management_number;
	}
	public void setManagement_number(String management_number) {
		this.management_number = management_number;
	}




}
