package Sec02;

public class For3 {

	public static void main(String[] args) {

		int i;
		int j;
		for (i = 0, j = 10; i < j; i++, j--) {
			System.out.printf("i=%d, j=%d\n", i, j);

		}
		System.out.println("-----------------------------");
		System.out.printf("i=>%d, j=>%d\n", i, j);

	}

}
