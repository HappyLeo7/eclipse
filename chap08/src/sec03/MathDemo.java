package sec03;

public class MathDemo {
	public static void main(String[] args) {
		System.out.println("2^8승 :" + Math.pow(2, 8)); // 2^8승
		System.out.println("0.0~1.0미만의 랜덤수 : " + Math.random()); // 0.0~1.0미만의 랜덤수
		System.out.println("실수 a의 sine값 : " + Math.sin(Math.PI)); // 실수 a의 sine값
		System.out.println("a와b 중 작은값 : " + Math.min(10, 20)); // a와b 중 작은값
	}
}
